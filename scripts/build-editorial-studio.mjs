#!/usr/bin/env node

import { readFile, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';
import process from 'node:process';

const root = resolve(import.meta.dirname, '..');
const outputPath = resolve(root, 'editorial/articles.js');

// Keep the Writing index's current public ordering explicit and reviewable.
const articleSources = [
  {
    slug: 'business-process-automation',
    sourcePath: 'blog/business-process-automation/index.html',
    dateLabel: 'June 2026',
  },
  {
    slug: 'how-we-work',
    sourcePath: 'how-we-work/index.html',
    dateLabel: 'May 2026',
  },
  {
    slug: 'where-we-help',
    sourcePath: 'where-we-help/index.html',
    dateLabel: 'May 2026',
  },
  {
    slug: 'different',
    sourcePath: 'blog/different/index.html',
    dateLabel: 'June 2026',
  },
];

function decodeHtml(value) {
  return value
    .replace(/&nbsp;/g, ' ')
    .replace(/&amp;/g, '&')
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>');
}

function textFromHtml(value) {
  return decodeHtml(value.replace(/<[^>]+>/g, ' ').replace(/\s+/g, ' ').trim());
}

function extractRequired(html, expression, label, sourcePath) {
  const match = html.match(expression);
  if (!match) {
    throw new Error(`Could not find ${label} in ${sourcePath}`);
  }
  return match[1].trim();
}

function firstArticleParagraph(articleHtml, sourcePath) {
  const afterHeading = articleHtml.replace(/^[\s\S]*?<\/h1>/i, '');
  return extractRequired(afterHeading, /<p(?:\s[^>]*)?>([\s\S]*?)<\/p>/i, 'first article paragraph', sourcePath);
}

function extractArticle(source, html) {
  if (typeof source.dateLabel !== 'string' || !source.dateLabel.trim()) {
    throw new Error(`Expected an explicit date label in the source manifest for ${source.sourcePath}`);
  }
  const articleHtml = extractRequired(html, /<main id="main">\s*([\s\S]*?)\s*<\/main>/i, 'article main', source.sourcePath);
  const titleHtml = extractRequired(articleHtml, /<h1(?:\s[^>]*)?>([\s\S]*?)<\/h1>/i, 'article title', source.sourcePath);
  const excerptHtml = firstArticleParagraph(articleHtml, source.sourcePath);
  const sectionCount = (articleHtml.match(/<h2(?:\s[^>]*)?>/gi) || []).length;

  if (sectionCount < 1) {
    throw new Error(`Expected at least one section heading in ${source.sourcePath}`);
  }

  return {
    slug: source.slug,
    sourcePath: source.sourcePath,
    title: textFromHtml(titleHtml),
    dateLabel: source.dateLabel,
    excerpt: textFromHtml(excerptHtml),
    sectionCount,
    articleHtml,
  };
}

async function buildPayload() {
  const articles = await Promise.all(articleSources.map(async (source) => {
    const html = await readFile(resolve(root, source.sourcePath), 'utf8');
    return extractArticle(source, html);
  }));

  return `/* Generated by scripts/build-editorial-studio.mjs. Do not edit by hand. */\nwindow.EDITORIAL_ARTICLES = ${JSON.stringify(articles, null, 2)};\n`;
}

const payload = await buildPayload();
const checkOnly = process.argv.includes('--check');

if (checkOnly) {
  const existing = await readFile(outputPath, 'utf8').catch(() => '');
  if (existing !== payload) {
    console.error('Editorial article payload is stale. Run: node scripts/build-editorial-studio.mjs');
    process.exitCode = 1;
  } else {
    console.log(`Editorial article payload verified: ${articleSources.length} articles`);
  }
} else {
  await writeFile(outputPath, payload);
  console.log(`Editorial article payload written: ${articleSources.length} articles`);
}
